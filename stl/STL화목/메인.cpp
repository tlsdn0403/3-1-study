//[문제] "메인.cpp"의 내용 중에 소문자를 모두 대문자로 바꿔 "메인 대문자.cpp"에 저장하라

int main()  
//----------  
{  
   ifstream in{ "메인.cpp" };  
   if (!in)  
       return 12345;  
   ofstream out{ "메인 대문자.cpp" };  
   
   transform(istreambuf_iterator<char>{in}, {},
       ostreambuf_iterator<char>{out}, [](char c) {return toupper(c); });
   
   return 0;  
}